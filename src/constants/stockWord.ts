export const GetDetailItemDescription = (label: string) => {
  switch (label) {
    case "PER":
      return `
PER（Price Earnings Ratio：株価収益率）:
株価が1株当たり純利益（EPS）の何倍になっているかを示す指標です。
企業の収益力に対して現在の株価が割安か割高かを判断する目安として用いられます。
一般的にPERが低いほど割安、高いほど割高と判断されますが、業種や企業の成長段階によって適正水準が異なります。
`;
    case "PBR":
      return `
PBR（Price Book-value Ratio：株価純資産倍率）:
株価が1株当たり純資産（BPS）の何倍になっているかを示す指標です。
企業の資産価値から見て株価が割安か割高かを判断する目安として用いられます。
PBRが1倍を下回ると、企業の資産をすべて売却して株主に分配した場合に、理論上は株価よりも多くのお金が戻ってくることを意味し、割安と判断されることがあります。
`;
    case "EPS":
      return `
EPS（Earnings Per Share：1株当たり純利益）:
企業が発行している株式1株当たり、どれくらいの純利益を生み出しているかを示す指標です。
「当期純利益 ÷ 発行済株式数」で計算されます。
EPSが高いほど、企業の収益力が高いと判断できます。
`;
    case "BPS":
      return `
BPS（Book-value Per Share：1株当たり純資産）:
企業が発行している株式1株当たり、どれくらいの純資産があるかを示す指標です。
「純資産 ÷ 発行済株式総数」で計算されます。
BPSが高いほど、企業の安定性が高いと判断されます。企業が解散・清算した場合に株主に残る価値の目安となります。
`;
    case "ROE":
      return `
ROE（Return On Equity：自己資本利益率）:
株主が投下した自己資本に対して、企業がどれだけの利益を生み出しているかを示す指標です。
「当期純利益 ÷ 自己資本」で計算されます。
ROEが高いほど、自己資本を効率的に活用して利益を上げている、経営効率の良い企業であると判断されます。
`;
    case "ROA":
      return `
ROA（Return On Assets：総資産利益率）:
企業が保有する総資産（自己資本＋負債）をどれだけ効率的に活用して利益を生み出しているかを示す指標です。
「当期純利益 ÷ 総資産」で計算されます。
ROAが高いほど、会社の全ての資産を効率的に使って利益を出していると判断されます。
`;
    case "自己資本比率":
      return `
自己資本比率:
企業の財務体質の健全性を示す指標です。総資本（負債＋自己資本）のうち、返済義務のない自己資本が占める割合を指します。自己資本比率が高いほど、負債が少なく、財務が安定していると言えます。
`;
    case "営業利益率":
      return `
営業利益率:
企業が本業でどれだけ効率的に利益を上げているかを示す指標です。売上高に占める営業利益（売上高から売上原価と販売費および一般管理費を差し引いた利益）の割合をパーセンテージで表します。この数値が高いほど、本業の収益性が高いと判断されます。
`;
    case "PEGレシオ":
      return `
PEGレシオ (Price Earnings Growth Ratio):
株価の割安・割高を判断する際に、企業の利益成長率を考慮した指標です。予想株価収益率（PER）を1株当たりの予想利益成長率で割って算出します。一般的に、PEGレシオが1倍以下であれば割安、2倍以上であれば割高と判断されることが多いです。
`;
    case "ICR":
      return `
ICR (Interest Coverage Ratio):
企業が利息支払いをどの程度まかなえるかを示す指標です。営業利益を支払利息で割って算出します。この数値が高いほど、企業の借入金の返済能力が高いとされます。
`;
    case "ベータ値":
      return `
ベータ値:
個別銘柄やファンドの株価が、株式市場全体（日経平均株価やTOPIXなど）の動きに対して、どの程度敏感に反応するかを示す指標です。ベータ値が1より大きい場合は市場全体よりも値動きが大きく（ハイリスク・ハイリターン）、1より小さい場合は市場全体よりも値動きが小さい（ローリスク・ローリターン）傾向があることを示します。
`;
    case "フリーCF":
      return `
フリーCF (Free Cash Flow：フリーキャッシュフロー):
企業が事業活動で得た現金のうち、事業の維持や成長に必要な投資を行った後に、企業が自由に使える余剰資金のことです。営業キャッシュフローから投資キャッシュフローを差し引くことで簡易的に計算されます。フリーキャッシュフローが多いほど、企業に財務的な余裕があり、株主への還元やさらなる成長投資に充てられる資金が多いことを示します。
`;
    default:
      return "";
  }
};
